def binary_search1(p,x,x_dim):
    idx_min = 0
    idx_max = x_dim - 1
    
    idx_m = int((idx_min + idx_max) / 2)
    
    if p < x[idx_min] or p > x[idx_max] : 
        raise ValueError
    
    while idx_min < idx_m :
        if p <= x[idx_m] : 
            idx_max = idx_m
        else : 
            idx_min = idx_m
        idx_m = int((idx_min + idx_max) / 2)
    return idx_min, idx_max

def find_range1(p,x,x_dim):
  for ii in range(2,x_dim):
    if p < x[ii-1] :
      idx_min = ii - 2
      idx_max = ii - 1
      return idx_min, idx_max
  return x_dim-2, x_dim-1

